/**
 * Fetches LaunchMyNFT solana.js and patches mint instructions so `currencyMint`
 * is always set for SOL phases (Anchor throws "currencyMint not provided" when
 * omitted or null; simulation fails when null). Wrapped SOL mint is used when
 * the phase currency is null.
 *
 * Also replaces LaunchMyNFT's default mainnet RPC with LAUNCHMYNFT.rpcEndpoint
 * from js/collection-data.js and writes js/lmt-config.js for the embed.
 *
 * Run before deploy or local testing: node scripts/vendor-launchmynft-solana.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const collectionDataPath = path.join(root, "js", "collection-data.js");
const lmtConfigPath = path.join(root, "js", "lmt-config.js");

const WRAPPED_SOL = "So11111111111111111111111111111111111111112";
const LMT_DEFAULT_MAINNET_RPC = "https://rahel-v0lqwp-fast-mainnet.helius-rpc.com/";

const PATCHES = [
  {
    id: "mintCv3",
    needle: "currencyMint:u.currency||null",
    replacement: `currencyMint:u.currency!=null?u.currency:new Eo.PublicKey("${WRAPPED_SOL}")`,
    marker: `currencyMint:u.currency!=null?u.currency:new Eo.PublicKey("${WRAPPED_SOL}")`,
    expectedCount: 1,
  },
  {
    id: "mintCore",
    needle: "buyerPaymentTokenWallet:ye,referredBy:null}).remainingAccounts",
    replacement: `buyerPaymentTokenWallet:ye,currencyMint:s.currency!=null?s.currency:new fa.PublicKey("${WRAPPED_SOL}"),referredBy:null}).remainingAccounts`,
    marker: `buyerPaymentTokenWallet:ye,currencyMint:s.currency!=null?s.currency:new fa.PublicKey("${WRAPPED_SOL}")`,
    expectedCount: 1,
  },
  {
    id: "mintTm",
    needle: "buyerPaymentTokenWallet:fe,referredBy:null}).remainingAccounts",
    replacement: `buyerPaymentTokenWallet:fe,currencyMint:s.currency!=null?s.currency:new fa.PublicKey("${WRAPPED_SOL}"),referredBy:null}).remainingAccounts`,
    marker: `buyerPaymentTokenWallet:fe,currencyMint:s.currency!=null?s.currency:new fa.PublicKey("${WRAPPED_SOL}")`,
    expectedCount: 1,
  },
];

function readLaunchMyNftConfig() {
  const src = fs.readFileSync(collectionDataPath, "utf8");
  const pick = (key) => {
    const m = src.match(new RegExp(`${key}:\\s*"([^"]+)"`));
    if (!m) {
      throw new Error(`Could not parse ${key} from ${collectionDataPath}`);
    }
    return m[1];
  };

  return {
    scriptVersion: pick("scriptVersion"),
    rpcEndpoint: pick("rpcEndpoint"),
    collectionId: pick("collectionId"),
    ownerId: pick("ownerId"),
  };
}

function writeLmtConfig({ ownerId, collectionId, rpcEndpoint }) {
  const contents = `// Generated by scripts/vendor-launchmynft-solana.mjs — do not edit
window.ownerId = ${JSON.stringify(ownerId)};
window.collectionId = ${JSON.stringify(collectionId)};
window.rpcEndpoint = ${JSON.stringify(rpcEndpoint)};
`;
  fs.writeFileSync(lmtConfigPath, contents, "utf8");
  console.log(`Wrote ${lmtConfigPath}`);
}

function isFullyPatched(body, rpcEndpoint) {
  const currencyOk = PATCHES.every(
    (patch) => body.includes(patch.marker) && !body.includes(patch.needle)
  );
  const rpcOk = !body.includes(LMT_DEFAULT_MAINNET_RPC) && body.includes(rpcEndpoint);
  return currencyOk && rpcOk;
}

function applyCurrencyPatches(body) {
  for (const patch of PATCHES) {
    if (body.includes(patch.marker)) {
      console.log(`Patch ${patch.id} already applied`);
      continue;
    }

    const count = body.split(patch.needle).length - 1;
    if (count !== patch.expectedCount) {
      throw new Error(
        `Expected exactly ${patch.expectedCount} occurrence(s) of "${patch.needle}" for ${patch.id}, found ${count}. LaunchMyNFT may have updated their bundle; adjust scripts/vendor-launchmynft-solana.mjs`
      );
    }

    body = body.replace(patch.needle, patch.replacement);
    console.log(`Applied patch ${patch.id}`);
  }

  return body;
}

function applyRpcPatch(body, rpcEndpoint) {
  if (body.includes(rpcEndpoint) && !body.includes(LMT_DEFAULT_MAINNET_RPC)) {
    console.log("RPC patch already applied");
    return body;
  }

  const count = body.split(LMT_DEFAULT_MAINNET_RPC).length - 1;
  if (count === 0) {
    throw new Error(
      `Default LaunchMyNFT mainnet RPC not found (${LMT_DEFAULT_MAINNET_RPC}). The bundle may have changed.`
    );
  }

  body = body.replaceAll(LMT_DEFAULT_MAINNET_RPC, rpcEndpoint);
  console.log(`Applied RPC patch (${count} replacement(s))`);
  return body;
}

async function main() {
  const config = readLaunchMyNftConfig();
  writeLmtConfig(config);

  const version = config.scriptVersion;
  const outDir = path.join(root, "vendor", "launchmynft", version);
  const outFile = path.join(outDir, "solana.js");
  const url = `https://storage.googleapis.com/scriptslmt/${version}/solana.js`;

  let body;

  if (fs.existsSync(outFile)) {
    const existing = fs.readFileSync(outFile, "utf8");
    if (isFullyPatched(existing, config.rpcEndpoint)) {
      console.log(`LaunchMyNFT solana.js already patched (${version}), skip download`);
      return;
    }

    if (!existing.includes(PATCHES[0].needle)) {
      body = existing;
      console.log(`Re-patching existing LaunchMyNFT solana.js (${version})`);
    }
  }

  if (!body) {
    console.log(`Downloading ${url}`);
    const res = await fetch(url);
    if (!res.ok) {
      throw new Error(`Fetch failed ${res.status}: ${url}`);
    }
    body = await res.text();
  }

  body = applyCurrencyPatches(body);
  body = applyRpcPatch(body, config.rpcEndpoint);

  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(outFile, body, "utf8");
  console.log(`Wrote patched ${outFile} (${(body.length / 1e6).toFixed(2)} MB)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
